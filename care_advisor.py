"""AI-powered care advisor using RAG over a curated pet care knowledge base.

Retrieves the most relevant snippets via Gemini embeddings (`gemini-embedding-001`
with task-type-aware encoding) stored in a persistent ChromaDB collection, then
asks Google Gemini to review the day's plan strictly against retrieved guidance
and return structured warnings the Scheduler can surface alongside its
rule-based output.
"""

